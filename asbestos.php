            <h2>Asbestos Removal</h2>

            <p>Asbestos removal is performed according to regulated standards
            of practice required by jurisdictions of your State Government or
            the Federal Government (U.S. EPA) depending on the size or quantity
            of asbestos needing removal.</p>

            <p>RTS will inspect your home or building and advise as to the
            removal scenario. In many cases asbestos that is intact and in good
            condition may not necessarily need removal. On the other hand,
            asbestos materials subject to disturbance especially due to
            remodeling should be candidate for removal (sometimes called
            "abatement").</p>

            <p>Engineering controls and containment are employed to create a
            safe and environmentally correct removal scenario. Methods and
            practices such as "Glove bag" systems are used to remove asbestos
            pipe wrap. Critical barriers are also installed to contain air
