            <h2>Top Quality Mold Remediation Services</h2>


            <p>According to the U.S. EPA, "The purpose of mold remediation is
            to remove the mold and prevent human exposure and further
            contamination to building materials and furnishings." (see <a href=
            "http://www.epa.gov/mold/" target=
            "_blank">http://www.epa.gov/mold/</a>)</p>


            <p>When a mold growth scenario is substantial enough or
            circumstances require remediation methods and practices, the
            following are considerations in performing mold remediation:</p>


            <ul>
                <li>Containment - Using plastic barriers or enclosures to
                confine and control the working area and minimize further
                airborne contamination in other portions of the building.</li>


                <li>Air controls - The use of negative air pressures during
                remediation help control air by maintaining a flow of air
                through a filtered air handler assembly situated in the
                containment area and exhausted to outdoors. The large volume of
                air which is flowing through the negative air unit provides a
                continuous draw of negative pressure which removes all
                particulate from the air continuously.</li>


                <li>Anti-fungal treatments - Cleaning solutions are used in the
                remediation process which kill active mold growth and render
                remaining mold spores non-viable to grow mold.. Anti-fungal
                treatments and sealants are applied to wall cavities and walls
                as part of the remediation process for example.Protective gear-
                Those performing remediation will wear protective clothing or
                respiratory protection in many of the remediation processes
                when exposure to mold will occur.</li>
