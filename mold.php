            <h3>Surface and Air Sampling Practices</h3>

            <p>Air tests are performed by collecting a known volume of airborne
            particulate through a special cartridge which contains a
            pre-treated slide. The air is pumped at a known rate which passes
            over this slide depositing mold spores resident in the rooms being
            tested. Samples are taken inside the subject areas and outside as
            well in order to gain a reference to compare types of molds
            (Qualitative) and concentration (Quantitative) mold levels. The
            indoor and outdoor results are compared to indicate if there is an
            abnormal difference between indoor vs. outdoor.</p>

            <h4>RTS Utilizes a Third-Party Nationally Recognized and Accredited
            Laboratory</h4>

            <p>A surface sample taken by a swab, tape slide, or by bulk
            sampling can be microscopically analyzed (called microanalysis) to
            determine the mold type. By using a third party laboratory, you can
            be sure that the results of our testing are genuine.</p>
        </div>

